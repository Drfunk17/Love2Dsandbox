function Map:checkCollision(x, y)
    local gridX = math.floor(x / self.tileSize) + 1
    local gridY = math.floor(y / self.tileSize) + 1

    return self.grid[gridY] and self.grid[gridY][gridX] == 1
end

function Map:draw()
    for y = 1, self.height do
        for x = 1, self.width do
            local tile = self.grid[y][x]
            if tile == 1 then
                love.graphics.setColor(0, 0, 255) -- Blue walls
                love.graphics.rectangle("fill", (x - 1) * self.tileSize, (y - 1) * self.tileSize, self.tileSize,
                    self.tileSize)
            end
        end
    end
end

return Map
